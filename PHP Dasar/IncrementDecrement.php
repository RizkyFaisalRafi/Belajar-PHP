<?php
// PHP mendukung gaya bahasa pemrograman C untuk menaikkan dan menurunkan data number sejumlah 1 angka
// Ini bisa mempersingkat kita ketika ingin menaikkan data

// Pre Increment
$a = 10;
$b = ++$a;
var_dump($b);
var_dump($a);
echo "\n";

// Post Increment
$a = 10;
$b = $a++;
var_dump($b);
var_dump($a);
echo "\n";

// Pre Decrement
$a = 10;
$b = --$a;
var_dump($b);
var_dump($a);
echo "\n";

// Post Increment
$a = 10;
$b = $a--;
var_dump($b);
var_dump($a);
echo "\n";

?>